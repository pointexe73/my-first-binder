# INPUT NUMEROS

# entrada de datos por teclado (-- input() --)
numero = input("Ingresa un numero: ")
numero1 = input("Ingresa otro numero: ")

# convierte a entero y multiplica el valor de la variable por 2
resultado_entero = int(numero) * 2

# convierte a flotante y multiplica el valor de la variable por 2
resultado_flotante = float(numero1) * 2

# imprime el valor de la variable
print("El numero es:", resultado_entero)
print("El numero es:", resultado_flotante)