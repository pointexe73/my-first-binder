# IF ELSE

# Edad del usuario
edad = 17

# Evaluar si el usuario es mayor de edad
if edad >= 18:
    # Si es así, imprimir el mensaje
    print("Eres mayor de edad")
else:
    # Si no, imprimir el mensaje
    print("Eres menor de edad")
    
# Fuera de la condicional
print("Fin del programa")

# Validación de contraseña

# Contraseña almacenada en el sistema
contraseña_almacenada = "FooBar"

# Contraseña ingresada por el usuario
contraseña_escrita = "FooBar"

# Evaluar si las contraseñas son iguales
if contraseña_almacenada == contraseña_escrita:
    # Si son iguales, imprimir el mensaje
    print("Las contraseñas coinciden")
else:
    # Si no son iguales, imprimir el mensaje
    print("Las contraseñas no coinciden")
