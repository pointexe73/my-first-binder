# FUNCIONES

# Lista de números
numeros = [4, 7, 1, 42, 15]

# Encontrar el número más alto en la lista
numero_mas_alto = max(numeros)
# Imprimir el número más alto
print(f'El número más alto es: {numero_mas_alto}')

# Encontrar el número más bajo en la lista
numero_mas_bajo = min(numeros)
# Imprimir el número más bajo
print(f'El número más bajo es: {numero_mas_bajo}')
