# suma y resta (+ y -)
suma = 12 + 5
resta = 12 - 5

# multiplicacion y division (* y /)
multiplicacion = 12 * 5
division = 12 / 5

# potenciacion (**)
exponente = 12 ** 5

# division entera (//)
division_entera = 12 // 5

# resto o modulo (%)
resto = 12 % 5

print(f"Suma: {suma}")
print(f"Resta: {resta}")
print(f"Multiplicacion: {multiplicacion}") 
print(f"Division: {division}")
print(f"Exponente: {exponente}")
print(f"Division Entera: {division_entera}")
print(f"Resto: {resto}")